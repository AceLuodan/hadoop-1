package com.calcite.memory;

/**
 * 列命映射
 */
public class Column
{
    public String name;
    public String type;

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public String getType()
    {
        return type;
    }

    public void setType(String type)
    {
        this.type = type;

    }
}
